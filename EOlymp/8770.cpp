using namespace std;

void solve() {
    int a, b, count, limit;
    cin >> a >> b >> count >> limit;

    cout << (min(a, b) >= count && (a + b + count - 1) / count <= limit ? "YES\n" : "NO\n");
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}