void solve() {
    string s;
    getline(cin, s);

    int res = 0;
    for (char c : s)
        res += (bool)isdigit(c);

    cout << res << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;
    cin.ignore();

    for (int i = 0; i < n; i++)
        solve();
}