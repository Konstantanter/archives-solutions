#include <string>
using namespace std;

void solve() {
    int size;
    cin >> size;

    long long MOD = 1e9 + 7;

    long long res = size;
    for (int i = size - 1; i; i--)
        res = (res + i + res * i) % MOD;
    
    cout << res << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}