#include <iostream>
#include <algorithm>
#include <vector>
#include <set>
#include <map>
#include <string>
using namespace std;

void solve(int test) {
    int playerCount, firstPlayer, passCount;
    cin >> playerCount >> firstPlayer >> passCount;
    cout << "Case " << test << ": " << (firstPlayer - 1 + passCount) % playerCount + 1 << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int test = 1; test <= n; test++)
        solve(test);
}