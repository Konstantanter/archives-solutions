void solve(int test) {
    long long maxBase, divisor;
    cin >> maxBase >> divisor;

    long long res = maxBase / divisor - (maxBase % divisor == 0);

    cout << "Case " << test << ": " << res << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int test = 1; test <= n; test++)
        solve(test);
}