#include <string>
using namespace std;

void solve() {
    string s;
    int shift;
    cin >> s >> shift;

    for (char &c : s)
        c = (c - 'A' + 26 - shift) % 26 + 'A';

    cout << s << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}